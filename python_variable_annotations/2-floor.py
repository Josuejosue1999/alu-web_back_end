#!/usr/bin/env python3
"""
This module contains a function `floor` that
calculates the floor of a floating-point number.
"""

import math


def floor(n: float) -> int:
    """
    This function takes a float and returns the floor of the float.
    Args:
        n (float): The floating-point number.
    Returns:
        int: The floor of the given float.
    """
    return math.floor(n)
