#!/usr/bin/env python3
"""
This module contains a function `to_str` that converts a float to its string representation.
"""

def to_str(n: float) -> str:
    """
    This function takes a float and returns its string representation.
    Args:
        n (float): The floating-point number.
    Returns:
        str: The string representation of the float.
    """
    return str(n)
